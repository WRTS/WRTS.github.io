$('#slideCheckbox01').on('click', function(){
    isChecked = $(this).is(':checked')
    if(isChecked){ 
        $("#myText1").attr("disabled", false);
    }
    else{ 
        $("#myText1").attr("disabled", true);
        $('#snomed-dropdown01').empty();
        $('#myText1').val('');
        $('#myText1').attr('placeholder', 'Text here');
    }
})

